package com.svrinfotech.pojo;

public class Drawer {

    private int icon;
    private String name;

    public Drawer() {

    }

    public Drawer(int icon, String name) {
        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}